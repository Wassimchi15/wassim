    private void btnselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselectActionPerformed
       
        try {
            String sq="SELECT `receive_no`, `receive_date`, `item_number`, `item_name`, `description`, `quentity`, `expire_date`, `buying_price`, `selling_price`, `total_price` FROM `receivestock` WHERE receive_no='"+txtsearch.getText()+"'";
           
            pst=conn.prepareStatement(sq);
            rs=pst.executeQuery();
            while(rs.next()){
                txtreno.setText(rs.getString("receive_no"));
                ((JTextField)datereceive.getDateEditor().getUiComponent()).setText(rs.getString("receive_date"));
                txtitemno.setText(rs.getString("item_number"));
                txtitemname.setText(rs.getString("item_name"));
                txtdescription.setText(rs.getString("description"));
                txtquentity.setText(rs.getString("quentity"));
                ((JTextField)dateexpire.getDateEditor().getUiComponent()).setText(rs.getString("expire_date"));
                txtbuyprice.setText(rs.getString("buying_price"));
                txtsellprice.setText(rs.getString("selling_price"));
                txttotalprice.setText(rs.getString("total_price"));
                
            }
        } catch (Exception e) {
        }
        
        jPanel2.removeAll();
        jPanel2.repaint();
        jPanel2.revalidate();
        
        jPanel2.add(jPanel3);
        jPanel2.repaint();
        jPanel2.revalidate();
        
        btnupdate.setEnabled(true);
        seDeAddDisable();
        barcode();
        
    }//GEN-LAST:event_btnselectActionPerformed

